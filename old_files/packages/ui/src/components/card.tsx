import * as React from "react";

import { cn } from "@workspace/ui/lib/utils";
import { BaseLink, type LinkProps } from "@workspace/ui/components/link";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-lg border bg-card text-card-foreground shadow-sm",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

// Polymorphic LinkedCard

type LinkedCardProps<C extends React.ElementType = "a"> =
  React.PropsWithChildren<{
    className?: string;
    as?: C;
  }> &
    Omit<React.ComponentPropsWithoutRef<C>, "children" | "className">;

const LinkedCard = React.forwardRef(
  <C extends React.ElementType = "a">(
    { className, as, children, ...props }: LinkedCardProps<C>,
    ref: React.Ref<any>
  ) => (
    <BaseLink
      as={as}
      ref={ref}
      className={cn(
        "rounded-lg border bg-card text-card-foreground shadow-sm",
        className
      )}
      {...props}
    >
      {children}
    </BaseLink>
  )
);
LinkedCard.displayName = "LinkedCard";

